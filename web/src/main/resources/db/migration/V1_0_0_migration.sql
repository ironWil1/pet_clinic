create table clinical_examination
(
    id          int8 generated by default as identity,
    date        date,
    is_can_move boolean,
    text        varchar(2048),
    weight      float8,
    doctor_id   int8,
    pet_id      int8,
    primary key (id)
);
create table comment
(
    id        int8 generated by default as identity,
    content   varchar(2048) not null,
    date_time timestamp    not null,
    user_id   int8,
    primary key (id)
);
create table comment_reaction
(
    positive   boolean not null,
    comment_id int8    not null,
    user_id    int8    not null,
    primary key (comment_id, user_id)
);
create table diagnosis
(
    id          int8 generated by default as identity,
    description varchar(2048) not null,
    doctor_id   int8,
    pet_id      int8,
    primary key (id)
);
create table doctor_non_working
(
    id        int8 generated by default as identity,
    date      date,
    type      varchar(255),
    doctor_id int8,
    primary key (id)
);
create table doctor_review
(
    id         int8 generated by default as identity,
    comment_id int8,
    doctor_id  int8,
    primary key (id)
);
create table medicine
(
    id               int8 generated by default as identity,
    description      varchar(2048) not null,
    icon             varchar(255),
    manufacture_name varchar(255) not null,
    name             varchar(255) not null,
    primary key (id)
);
create table notification
(
    id          int8 generated by default as identity,
    description varchar(2048),
    end_date    timestamp,
    event_id    varchar(255),
    location    varchar(255),
    start_date  timestamp,
    summary     varchar(255),
    pet_id      int8,
    primary key (id)
);
create table pet_entities
(
    pet_type    varchar(31)  not null,
    id          int8 generated by default as identity,
    avatar      varchar(255),
    birth_day   date         not null,
    breed       varchar(255) not null,
    color       varchar(255),
    description varchar(2048),
    gender      varchar(255) not null,
    name        varchar(255) not null,
    pet_size    varchar(255),
    weight      float8,
    client_id   int8,
    primary key (id)
);
create table pet_contact
(
    id         int8 not null,
    address    varchar(255),
    owner_name varchar(255),
    pet_code   varchar(255),
    phone      int8,
    primary key (id)
);
create table pet_found
(
    id        int8 generated by default as identity,
    latitude  varchar(255),
    longitude varchar(255),
    text      varchar(255),
    pet_id    int8,
    primary key (id)
);
create table procedure
(
    type                  varchar(31) not null,
    id                    int8 generated by default as identity,
    date                  date,
    is_periodical         boolean,
    medicine_batch_number varchar(255),
    period_days           int4,
    medicine_id           int8,
    notification_id       int8,
    pet_id                int8,
    primary key (id)
);
create table reproduction
(
    id           int8 generated by default as identity,
    child_count  int4,
    due_date     date,
    estrus_start date,
    mating       date,
    pet_id       int8,
    primary key (id)
);
create table role
(
    name varchar(25) not null,
    primary key (name)
);
create table topic
(
    id               int8 generated by default as identity,
    content          varchar(2048),
    creation_date    timestamp not null,
    is_closed        boolean,
    last_update_date timestamp not null,
    title            varchar(255),
    topic_starter_id int8,
    primary key (id)
);
create table topic_comments
(
    topic_id    int8 not null,
    comments_id int8 not null
);
create table treatment
(
    id           int8 generated by default as identity,
    diagnosis_id int8,
    primary key (id)
);
create table treatment_procedure_list
(
    treatment_id      int8 not null,
    procedure_list_id int8 not null
);
create table user_entities
(
    user_type varchar(31)  not null,
    id        int8 generated by default as identity,
    avatar    varchar(255),
    email     varchar(255) not null,
    enabled   boolean,
    firstname varchar(255) not null,
    lastname  varchar(255) not null,
    password  varchar(255) not null,
    primary key (id)
);
create table user_role
(
    role_name varchar(25),
    user_id   int8 not null,
    primary key (user_id)
);
create table verification_token
(
    id      int8 not null,
    user_id int8 not null,
    primary key (id)
